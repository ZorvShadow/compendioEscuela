		<section>
			<h3>Teorias del Origen de la Vida</h3>
			<p>
				Desde que el ser humano apareció en la tierra, ha intentado explicar el origen de la vida.
				Al conocer esas explicaciones es posible predecir el futuro de la vida en nuestro planeta.
			</p>
			<p>
				Las principales teorías del origen de la vida son: creacionismo, generación espontánea,
				panspermia y la teoría quimiosintética.
			</p>
			<section>
				<h4>Creacionismo</h4>
				<p>
					Todas las religiones del mundo sostienen que un dios o varios dioses fueron los creadores
					de la vida en la tierra. Los relatos míticos revelan el origen sagrado de la vida; cada
					cultura con sus propios dioses, tiene una versión de cómo nació la vida para todos los
					seres vivos.
					<strong> En resumen, es la teoría que se centra alrededor de la religión. </strong>
				</p>
			</section>
			<section>
				<h4>Generación espontánea</h4>
				<p>
					En el siglo <span>V a.n.e.</span> , grandes científicos como Anaximandro y Tales de Mileto
					plantearon la idea de la
					<strong>teoría de la generación espontánea</strong> como respuesta a la pregunta del
					origen de la vida. Creían que la vida se originaba en el lodo, de la combinación de
					elementos, pero sin la intervención de los dioses. En el siglo
					<span>IV a.n.e.</span>, Platón y Aristóteles respaldaron la concepción de que la vida
					surgía a partir del rocío, el agua de mar, el sudor, etc., esto explicaba que la
					interacción de la materia inorgánica se debía a una fuerza natural a la que Aristóteles
					llamó <em>entelequia</em>, capaz de dar vida a lo que carecía de ella. En el siglo
					<span>II a.n.e.</span>, el escritor romano Virgilio sostuvo que las abejas se originaban
					de la miel.
				</p>
				<p>
					La teoría de la generación espontánea mantuvo vigencia durante más de 1500 años, durante
					los cuales la Iglesia la ligó al concepto de
					<em>vitalismo</em>, que es la creencia de que la vida se origina de una fuerza vital que
					no puede ser explicada por la ciencia.
				</p>
				<p>
					Se decía entonces que si una hoja caía al mar se convertía en pez; si caía a la tierra se
					transformaba en ave.
				</p>
				<hr />
				<p>
					En 1668, el médico italiano Francesco Redi realizó un experimento sencillo con el que <strong
						>refutó</strong
					> la teoría de la generación espontánea. Utilizando tres frascos de vidrio, colocó en cada
					uno carne de res, pero en uno de ellos la cubrió con una grasa, en otro con una tapa y en el
					último la dejó destapada. Después de varios días, observó que en el frasco destapado se habían
					formado larvas, en el frasco tapado no había larvas y en el frasco cubierto con la grasa había
					larvas en la superficie de la grasa, pero no en la carne. Con este experimento, Redi demostró
					que las larvas provenían de los huevos de las moscas que habían puesto sus huevos en la carne.
				</p>
			</section>
			<section>
				<h4>Panspermia de "Arrhenius"</h4>
				<p>
					Esta teoría fue propuesta en 1879 por Herman von Helmholtz, pero en 1908, el químico sueco
					Svante Arrehenius, la popularizó. De acuerdo con ella, la aparición de la vida en la
					Tierra se basa en que la vida se originó en el espacio y los primeros seres vivos fueron
					esporas y bacterias que llegaron a la Tierra en meteoritos que chocaron con ella. <strong>
						Principalmente trata de que la vida básica viene del espacio exterior que después
						encontró las condiciones necesarias para evolucionar en el planeta Tierra.
					</strong>
				</p>
			</section>
			<section>
				<h4>Teoría quimiosintética</h4>
				<p>
					Se fundamenta en la hipótesis de que la vida en nuestro planeta se originó a partir de la
					sintésis de moléculas muy primitivas en el origen de los tiempos y que fueron
					complejizándose hasta formar las primeras células.
				</p>
				<p>
					Charles Darwin nunca publicó nada sobre el origen de la vida. Sin embargo, en una carta
					encontrada en 1935, Darwin escribió:
				</p>
				<blockquote>
					…si pudiéramos concebir un pequeño charco de agua templada, con toda clase de sales
					nitrogenadas y fosfóricas, y con luz, calor y electricidad, se formaría químicamente de
					allí un compuesto proteínico capaz de sufrir transformaciones aún más complejas, pero hoy
					en día un compuesto así sería inmediatamente devorado o absorbido; y esto no hubiera
					ocurrido antes de que aparecieran los seres vivos…
				</blockquote>
				<p>
					En 1921, el bioquímico ruso Alexander Oparin, concluyó que la formación de los primeros
					compuestos orgánicos tuvo un origen abiótico y que aquellos se habían generado a partir de
					sustancias inorgánicas. Tenía pruebas de que la atmósfera era muy diferente de la actual,
					ya que no contenía oxígeno lubre, sino hidrógeno, metano y amoniaco, lo que la hacía <strong
						>reductora</strong
					>. Tales elementos reaccionaron entre sí gracias a la energía calorífica del Sol, las
					erupciones, la energía eléctrica de las tormentas, etc. y formaron moléculas orgánicas
					simples como aminoácidos, azúcares, ácidos grasos, etc. Estas moléculas se acumularon en
					los mares primitivos y formaron una sopa orgánica.
				</p>
				<p>Hasta ahora es la teoría del origen de la vida más aceptada.</p>
