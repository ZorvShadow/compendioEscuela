      <h3>Regionalización en el contexto de la globalización:</h3>
      <p class="justificar samesubtopic">
        La década de los 90s fueron una conyuntura Mundial y un cambio de
        estructuras en las actividades económicos en todo el mundo derivados de
        la creciente internacionalización y la globalización , la cual se
        refleja a través del aumento en el comercio y de la producción mundial,
        desregulación de mercados financieros, innovación tecnológica e
        informática y la generalización de los servicios. Esto radico en la
        aparición de bloques en el mundo cuyo objetivo era consolidar el
        intercambio e integración, surgiendo 3 regiones o bloques principales.
      </p>
      <p class="justificar samesubtopic">
        De esta forma surgen tratados y acuerdos que intentan crear mejores
        condiciones de intercambio y competitividad, pero para la mayoría de los
        países subdesarrollados significó aumentar dependencia hacia los países
        desarrollados y relaciones comerciales aprovechadas por un pequeño
        sector empresarial.
      </p>
      <strong> Etapas:</strong>
      <ul class="orderedlist">
        <li>
          Primera: Incorporación una zona libre de aranceles o una zona de
          impuestos, donde los Estados signatarios establecen los sectores
          libres de impuestos.
        </li>
        <li>
          Segunda: Unión Aduanera, la cual se expresa a través de un arancel
          común ante el resto del mundo.
        </li>
        <li>
          Tercera: Mercado Común, no se establece libertad en intercambio de
          bienes y servicios, libre movilidad de trabajo y capital. La expresión
          más avanzada del mercado común es unión económica donde existe una
          moneda común y la culminación de integración sería la integración
          total, donde ya hay unidad en criterios de política económica,
          cambiaria, tributaria y de precios.
        </li>
      </ul>
      <p class="justificar samesubtopic">
        Estos acuerdos se desarrollaron como parte de implementación del
        Neoliberalismo iniciado en el sexenio de Gorari. El primero de ellos fue
        el Tratado de Libre Comercio de América del Norte (TLCAN).
      </p>
      <p class="justificar samesubtopic">
        El cual esta integrado por EU, Canadá y México. Inició en 1994 y fue
        sustituido por el Tratado de México, EU y Canadá (TMEC) a partir de
        2018, aunque no se han acordado todos los apartados y seguirá vigente
        hasta su aprobación total. Este acuerdo integra a la región reduciendo
        el costo por la comercialización de algunos rubros o eximiendo los
        impuestos. La integración se planteaba gradual y ascendente, pero no
        avanzó en la integración de personas, ni el tránsito de fronteras como
        en la Unión Europea, por la disparidad de las economías.
      </p>
      <h3>El neoliberalismo y la globalización en México (1994-2018):</h3>
      <p lass="justificar samesubtopic">
        En 1994 el entorno económico mundial se encontraba inserto en la
        globalización y el modelo económico predominante era el Neoliberalismo.
        La imposicion de las circunstancias economicas en Mexico causo serios
        estragos como en sectores sociales vulnerables, que en 30 anos no han
        tenido ninguna oportunidad de desarrollo y sus condiciones se han
        complicado considerablemente
      </p>
      <p lass="justificar samesubtopic">
        Tras la falta de oportunidades de empleo, la perdida de derechos
        colectivos y la crisis del campo mexicano, la migracion hacia EU ha sido
        cada vez mayor, la pobreza y la pobreza extrema ha crecido
        significativamente, una elite empresasrial (nacional y extranjera) en
        conubernio con la elite politica se enriquecen desproporcionalmente.
      </p>
